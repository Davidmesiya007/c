// C program to Add Two Numbers

#include<stdio.h>
void main()
{

    int a,b,sum;
    printf("Addition\n");
    printf("Enter The First Number: ");
    scanf("%d",&a);
    printf("Enter The Second Number: ");
    scanf("%d",&b);
    sum = a + b;
    printf("The sum of %d and %d is = %d",a,b,sum);
}
